# Operador in e not in

# Operador in
# Verifica se um elemento está contido em uma lista
# Retorna True ou False
nome = 'Python'

if 'P' in nome:
    print('Existe a letra P em Python')
else:
    print('Não existe a letra P em Python')

# Operador not in
# Verifica se um elemento não está contido em uma lista
# Retorna True ou False
if 'P' not in nome:
    print('Existe a letra P em Python')
else:
    print('Não existe a letra P em Python')
