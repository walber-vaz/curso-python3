"""
Tipo int e float

int -> número inteiro
o tipo int representa qualquer número
positivo ou negativo. int sem sinal é considerado
positivo.

float -> número com ponto flutuante
o tipo float representa qualquer número 
positivo ou negativo com ponto flutuante
float sem senil é considerado positivo.

a função type mostra o tipo que o python
inferiu ao valor.

tudo em python e objeto
"""

# int
print(42)
print(-42)
print(0)

# float
print(1.1)
print(-1.1)
print(0.0)

# type "function" mais e uma class
print(type(0))
print(type(0.0))
print(type("w2k"))
